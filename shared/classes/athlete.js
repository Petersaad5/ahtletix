class Athlete {
    constructor(name, sport, age, gender, nationality, height, weight, team, image, information, birthDate, deathDate) {
        this.name = name;
        this.sport = sport;
        this.age = age;
        this.gender = gender;
        this.nationality = nationality;
        this.height = height;
        this.weight = weight;
        this.team = team;
        this.image = image;
        this.information = information;
        this.birthDate = birthDate;
        this.deathDate = deathDate;
    }
}

module.exports = Athlete;